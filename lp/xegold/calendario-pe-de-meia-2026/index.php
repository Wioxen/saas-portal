        <h2>Quando o dinheiro cai na conta de fato</h2>

        <p>O calendário do MEC é a <strong>data em que a parcela é liberada</strong>. O dinheiro entra na Caixa Tem em até <strong>3 dias úteis</strong> após a liberação. Se não entrou nesse prazo, é hora de checar:</p>

        <ul>
          <li><strong>Frequência ainda em apuração</strong> — escola não fechou os dados a tempo. A parcela vem na próxima janela retroativa.</li>
          <li><strong>Conta Caixa Tem não ativada</strong> — se for menor de 18, falta autorização do responsável. Saldo aparece, mas fica preso.</li>
          <li><strong>CPF errado no cadastro escolar</strong> — qualquer erro de dígito trava o repasse. Conferir na secretaria da escola.</li>
          <li><strong>Não está mais no CadÚnico</strong> — se a família saiu ou tem dado desatualizado, o cruzamento falha.</li>
        </ul>

