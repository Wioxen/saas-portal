      <h2>Perguntas frequentes</h2>

      <details class='faq-item'>
        <summary>O calendário do Pé-de-Meia é igual em todos os estados?</summary>
        <div class='faq-answer'>Sim. O cronograma é nacional, definido pelo MEC. O que pode mudar é o dia exato dentro da janela — algumas escolas mandam dados antes, outras na última hora. Mas o intervalo de pagamento é o mesmo em todo Brasil.</div>
      </details>

      <details class='faq-item'>
        <summary>Se eu tirar férias longas, perco a parcela?</summary>
        <div class='faq-answer'>O recesso escolar oficial não conta como falta — está previsto no calendário acadêmico e o sistema do MEC já reconhece isso. Já se você faltar fora dos períodos de recesso e ficar abaixo de 80% no mês, perde aquela parcela específica.</div>
      </details>

      <details class='faq-item'>
        <summary>Tem como antecipar uma parcela?</summary>
        <div class='faq-answer'>Não. O programa não permite adiantamento. Cada parcela é liberada na janela definida pela Portaria 169/2026. Sites ou pessoas que prometem "antecipação" ou "liberação rápida" são <strong>fraude</strong>.</div>
      </details>

      <details class='faq-item'>
        <summary>Quem fica no 3º ano em 2026 também recebe o incentivo de conclusão?</summary>
        <div class='faq-answer'>Sim. Se você concluir o 3º ano em 2026 com frequência mínima e participar do ENEM, recebe R$ 1.000 (conclusão) + R$ 200 (ENEM) ao final, somados ao saldo das parcelas de frequência. Pagamento da conclusão geralmente entre janeiro e março de 2027.</div>
      </details>

      <details class='faq-item'>
        <summary>O Pé-de-Meia é descontado se eu trabalhar?</summary>
        <div class='faq-answer'>Não. Aluno do ensino médio que trabalha (estágio, jovem aprendiz, MEI, carteira) continua recebendo, desde que a renda familiar per capita siga abaixo de meio salário mínimo no CadÚnico. O critério é da família, não do aluno individual.</div>
      </details>

      <details class='faq-item'>
        <summary>O dinheiro pode ser usado em qualquer coisa?</summary>
        <div class='faq-answer'>Sim. As parcelas de frequência e matrícula caem na Caixa Tem e podem ser usadas livremente — saque, transferência, pagamento. A diferença é o <strong>incentivo de conclusão</strong> (R$ 1.000/ano), que vai pra poupança e só libera no final do ensino médio.</div>
