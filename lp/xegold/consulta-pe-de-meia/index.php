      <h2>Perguntas frequentes</h2>

      <details class='faq-item'>
        <summary>Qual o app oficial do Pé-de-Meia em 2026?</summary>
        <div class='faq-answer'>É o <strong>Jornada do Estudante</strong>, do MEC. Disponível na Google Play e App Store. Não confunda com apps de terceiros que cobram taxa — esses são fraude. O Caixa Tem também é oficial, mas serve só para movimentar o dinheiro depois que ele cai.</div>
      </details>

      <details class='faq-item'>
        <summary>Posso consultar o Pé-de-Meia sem internet?</summary>
        <div class='faq-answer'>Sim. Ligue gratuitamente para <strong>0800 616 161</strong> (atendimento MEC). O atendente verifica pelo seu CPF se você é beneficiário e qual a situação das parcelas.</div>
      </details>

      <details class='faq-item'>
        <summary>Tem custo pra consultar?</summary>
        <div class='faq-answer'>Não. Consulta no app Jornada do Estudante, no portal MEC, no Caixa Tem ou pelo 0800 — <strong>tudo gratuito</strong>. Site ou pessoa que cobra "taxa" pra liberar o benefício é golpe.</div>
      </details>

      <details class='faq-item'>
        <summary>Quem cuida do dinheiro: o estudante ou o responsável?</summary>
        <div class='faq-answer'>O estudante é o titular da conta. Mas se for menor de 18 anos, precisa de autorização do responsável legal pra movimentar (sacar, transferir, pagar). Sem a autorização, o saldo aparece mas fica bloqueado pra uso.</div>
      </details>

      <details class='faq-item'>
        <summary>O Pé-de-Meia é o mesmo que o Bolsa Família?</summary>
        <div class='faq-answer'>Não. São programas diferentes. O Bolsa Família é da família, gerido pelo MDS (Ministério do Desenvolvimento Social). O Pé-de-Meia é específico do estudante de ensino médio público, gerido pelo MEC. Famílias podem receber os dois ao mesmo tempo, sem prejuízo.</div>
      </details>

      <details class='faq-item'>
        <summary>Demorou pra atualizar a frequência. É normal?</summary>
        <div class='faq-answer'>Sim. A escola tem um prazo (as <strong>15 datas-limite definidas pela Portaria 169/2026</strong>) para enviar os dados ao MEC. Se você consulta logo após o final do mês, normalmente ainda está em análise. Aguarde a próxima janela de pagamento — a parcela retroativa vem junto.</div>
      </details>

