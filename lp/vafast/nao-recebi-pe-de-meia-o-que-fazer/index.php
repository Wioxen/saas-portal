      <h2>Perguntas frequentes</h2>

      <details class='faq-item'>
        <summary>Posso recorrer se o Pé-de-Meia foi negado?</summary>
        <div class='faq-answer'>Sim, mas o "recurso" é prático: identificar qual dos 5 critérios você não cumpriu e corrigir esse ponto. Não há um processo formal de recurso jurídico — o sistema reavalia automaticamente todo mês. O caminho é: descobrir a causa, resolver, esperar o próximo ciclo de pagamento.</div>
      </details>

      <details class='faq-item'>
        <summary>O que acontece com as parcelas atrasadas quando eu resolvo o problema?</summary>
        <div class='faq-answer'>Em quase todos os casos, o pagamento volta retroativo. Se você foi elegível em março mas só foi reconhecido em junho (após resolver pendência), as parcelas de março, abril e maio são pagas juntas na próxima janela. A exceção é frequência: meses em que você ficou abaixo de 80% e não conseguiu justificar não retornam.</div>
      </details>

      <details class='faq-item'>
        <summary>Demoram para corrigir os dados da escola. Tem como acelerar?</summary>
        <div class='faq-answer'>Sim. Se a secretaria da sua escola não responde em 30 dias, escale para a Secretaria de Educação do estado (procure no Google "Secretaria de Educação [seu estado] ouvidoria"). Em paralelo, abra reclamação no Fala.BR. Reclamações no Fala.BR têm prazo legal de resposta — geralmente acelera bastante.</div>
      </details>

      <details class='faq-item'>
        <summary>Meu CadÚnico está atualizado mas o sistema diz que não está. O que fazer?</summary>
        <div class='faq-answer'>Esse problema costuma ser desincronia entre o CadÚnico do MDS e o sistema do MEC. A atualização do CRAS leva alguns dias para refletir. Se já se passaram mais de 30 dias da atualização e o MEC ainda mostra desatualizado, ligue para o 0800 616 161 e peça verificação manual com seu CPF — eles acessam o sistema em tempo real.</div>
      </details>

      <details class='faq-item'>
        <summary>Aluno faltou por causa de doença. Perde a parcela?</summary>
        <div class='faq-answer'>Não, se você apresentar atestado médico na secretaria da escola dentro do prazo (geralmente até 5 dias úteis após a falta). A escola registra como falta justificada, que não conta no cálculo de 80%. Sem atestado entregue, conta como falta normal.</div>
      </details>

      <details class='faq-item'>
        <summary>Posso pedir que o pagamento vá pra outra conta da Caixa?</summary>
        <div class='faq-answer'>Não. O recebimento é obrigatoriamente pela Caixa Tem aberta automaticamente em nome do estudante. Você pode, depois que o dinheiro entrar, transferir via PIX pra qualquer conta sua ou da família. A Caixa Tem é só o canal de entrada.</div>
      </details>

      <details class='faq-item'>
        <summary>Aluno mudou de cidade. Continua recebendo?</summary>
        <div class='faq-answer'>Sim, desde que continue matriculado em escola pública e a família atualize o endereço no CadÚnico. A mudança em si não desclassifica. O que pode atrasar 1-2 meses é a sincronização da nova escola com o sistema do MEC.</div>
      </details>

      <details class='faq-item'>
        <summary>Pais separados: qual deles autoriza a Caixa Tem do menor?</summary>
        <div class='faq-answer'>Quem detém a guarda legal do menor. Se a guarda é compartilhada, qualquer um dos dois pode fazer. Se é apenas com um, é esse que autoriza no Caixa Tem. Em casos de tutela ou guarda judicial diferente, leve a documentação à Caixa para análise específica.</div>
      </details>

